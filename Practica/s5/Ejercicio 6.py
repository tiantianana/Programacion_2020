# -*- coding: utf-8 -*-

i = 0

while 0 <= i <= 30:
    if (i % 2 == 0):
        if (i / 2 != 1):
            print(i)
    elif (i % 3 == 0):  
        if (i / 3 != 1):
            print(i)
    elif (i % 5 == 0):
         if (i / 5 != 1):
            print(i)
    elif (i % 7 == 0):
         if (i / 7 != 1):
            print(i)
    i += 1